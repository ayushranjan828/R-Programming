# Boolean Variable
a = TRUE
print(a)
print(class(a))
a = FALSE
print(a)
print(class(a))

# Numeric variable
b = 14
print(b)
print(class(b))

# Integer variable
c = 14L
print(c)
print(class(c))


# Floating variable
d = 14.26
print(d)
print(class(d))

# Character variable
e = "a"
print(e)
print(class(e))

# String variable
f = "Ayush Ranjan"
print(f)
print(class(f))