# Numeric Constants
a = 3e-3
print(a)
print(typeof(a))
print(class(a))

# Integer Constants
a = 14L
print(a)
print(typeof(a))
print(class(a))

# Boolean Constants
a = TRUE
print(a)
print(typeof(a))
print(class(a))

b = F
print(b)
print(typeof(b))
print(class(b))

# String Constants
a = "Ayush Ranjan"
print(a)
print(typeof(a))
print(class(a))

# Complex number Constants
a = 3.2e-1i
print(a)
print(typeof(a))
print(class(a))

